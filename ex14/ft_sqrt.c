int	ft_sqrt(int nb)
{
	int i;
	int res;

	i = 1;
	res = 1;
	if (nb == 0 || nb == 1)
		return (nb);
	while (i < nb)
	{
		if (i * i == nb)
		{
			return (i);
		}
		i++;
	}
	return (0);
}
